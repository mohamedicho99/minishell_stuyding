#include "push_swap.h"

void	ft_exit(t_stack **head)
{
	if (!head || !*head)
	{
		ft_printf("Error\n");
		exit(1);
	}
	ft_lstclear(head);
	ft_printf("Error\n");
	exit(1);
}
